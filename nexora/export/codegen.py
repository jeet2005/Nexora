def generate_code_all(report: NexoraReport, output_dir: str | Path):
    """Generate all code variants to a directory."""
    from nexora.export.codegen import (
        generate_python_script, generate_fastapi_app, generate_streamlit_app,
        generate_flask_app, generate_dockerfile, generate_mlflow_script, generate_gradio_app,
        generate_github_actions
    )
    import joblib
    
    out = Path(output_dir).expanduser().resolve()
    out.mkdir(parents=True, exist_ok=True)
    
    # Save the pipeline
    joblib.dump(report.pipeline, out / "pipeline.pkl")
    
    # Save all scripts
    (out / "model.py").write_text(generate_python_script(report), encoding="utf-8")
    (out / "fastapi_app.py").write_text(generate_fastapi_app(report), encoding="utf-8")
    (out / "streamlit_app.py").write_text(generate_streamlit_app(report), encoding="utf-8")
    (out / "flask_app.py").write_text(generate_flask_app(report), encoding="utf-8")
    (out / "Dockerfile").write_text(generate_dockerfile(report), encoding="utf-8")
    (out / "mlflow_train.py").write_text(generate_mlflow_script(report), encoding="utf-8")
    (out / "gradio_app.py").write_text(generate_gradio_app(report), encoding="utf-8")
    
    # Write Github actions
    github_dir = out / ".github" / "workflows"
    github_dir.mkdir(parents=True, exist_ok=True)
    (github_dir / "nexora_train.yml").write_text(generate_github_actions(report), encoding="utf-8")
    
    # Write basic requirements.txt
    reqs = "pandas\nscikit-learn\njoblib\nfastapi\nuvicorn\npydantic\nstreamlit\nflask\nmlflow\ngradio\n"
    (out / "requirements.txt").write_text(reqs, encoding="utf-8")
