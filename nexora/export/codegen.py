def _schema_to_pydantic_fields(schema) -> str:
    """Convert a Nexora schema to Pydantic field definitions."""
    lines = []
    for col in schema.feature_columns:
        if col.name == schema.target:
            continue
        
        # Map python types
        py_type = "float"
        if col.dtype_str.startswith("int"):
            py_type = "int"
        elif col.dtype_str.startswith("bool"):
            py_type = "bool"
        elif col.dtype_str in ("object", "category", "string"):
            py_type = "str"
            
        lines.append(f"    {col.name}: {py_type}")
    return "\n".join(lines)


def _schema_to_streamlit_inputs(schema) -> str:
    """Convert a Nexora schema to Streamlit input widgets."""
    lines = []
    for col in schema.feature_columns:
        if col.name == schema.target:
            continue
            
        if col.dtype_str in ("object", "category", "string"):
            # Mock a text input or selectbox. In reality we'd pull unique values.
            lines.append(f'        "{col.name}": st.text_input("{col.name}"),')
        elif col.dtype_str.startswith("int"):
            lines.append(f'        "{col.name}": st.number_input("{col.name}", step=1),')
        else:
            lines.append(f'        "{col.name}": st.number_input("{col.name}"),')
            
    return "\n".join(lines)


def generate_python_script(report: NexoraReport, model_name: str | None = None) -> str:
    """Generate a standalone Python script to run the model."""
    target_model = model_name or report.best_model
    
    code = f'''"""Generated by Nexora.
Task: {report.task_type}
Model: {target_model}
Score: {report.best_score:.4f} ({report.best_score_label})
"""

import pandas as pd
import joblib

# Note: For exact reproducible preprocessing, we load the trained pipeline.
# The pipeline handles all imputation, scaling, encoding, and the final estimator.

def predict(input_data: dict | pd.DataFrame) -> list:
    """Run predictions using the trained Nexora pipeline."""
    # Ensure input is a DataFrame
    if isinstance(input_data, dict):
        df = pd.DataFrame([input_data])
    else:
        df = input_data
        
    # Load pipeline
    try:
        pipeline = joblib.load("pipeline.pkl")
    except FileNotFoundError:
        raise FileNotFoundError("pipeline.pkl not found. Make sure to save it alongside this script.")
        
    return pipeline.predict(df).tolist()

if __name__ == "__main__":
    # Example usage
    sample_data = {{
{_schema_to_streamlit_inputs(report.schema).replace('st.text_input("', '"').replace('"),', '",').replace('st.number_input("', '0.0, # ').replace('", step=1),', '').replace('"),', '')}
    }}
    
    # You will need actual default values for the sample above to run successfully.
    print("Prediction:", predict(sample_data))
'''
    return code


def generate_fastapi_app(report: NexoraReport) -> str:
    """Generate a FastAPI app script."""
    
    fields = _schema_to_pydantic_fields(report.schema)
    
    code = f'''"""FastAPI service generated by Nexora."""

from fastapi import FastAPI, HTTPException
from pydantic import BaseModel
import pandas as pd
import joblib

app = FastAPI(
    title="Nexora Model API",
    description="Auto-generated predictive API for {report.target}",
    version="1.0.0"
)

# Load pipeline globally at startup
try:
    pipeline = joblib.load("pipeline.pkl")
except Exception as e:
    pipeline = None
    print(f"Warning: Could not load pipeline.pkl: {{e}}")

class PredictionInput(BaseModel):
{fields}

class PredictionOutput(BaseModel):
    prediction: float | str | int
    confidence: float | None = None

@app.get("/health")
def health_check():
    return {{"status": "ok", "model_loaded": pipeline is not None}}

@app.post("/predict", response_model=PredictionOutput)
def predict(data: PredictionInput):
    if pipeline is None:
        raise HTTPException(status_code=500, detail="Model pipeline not loaded.")
        
    # Convert input to DataFrame
    df = pd.DataFrame([data.model_dump()])
    
    # Predict
    pred = pipeline.predict(df)[0]
    
    # Calculate confidence if supported
    confidence = None
    if hasattr(pipeline, "predict_proba"):
        proba = pipeline.predict_proba(df)[0]
        confidence = round(float(max(proba)), 4)
        
    # Type cast for JSON serialization
    if hasattr(pred, "item"):
        pred = pred.item()
        
    return {{"prediction": pred, "confidence": confidence}}

if __name__ == "__main__":
    import uvicorn
    uvicorn.run(app, host="0.0.0.0", port=8000)
'''
    return code


