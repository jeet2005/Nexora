def generate_gradio_app(report: NexoraReport) -> str:
    """Generate a Gradio app script."""
    
    code = f'''"""Gradio app generated by Nexora."""

import gradio as gr
import pandas as pd
import joblib

# Load pipeline globally
try:
    pipeline = joblib.load("pipeline.pkl")
except Exception as e:
    pipeline = None
    print(f"Warning: Could not load pipeline.pkl: {{e}}")

def predict_fn(*args):
    if pipeline is None:
        return "Error: Model pipeline not loaded."
        
    # Zip feature names with args to create dict
    feature_names = {[col.name for col in report.schema.feature_columns if col.name != report.target]}
    input_data = dict(zip(feature_names, args))
    
    # Convert to DataFrame
    df = pd.DataFrame([input_data])
    
    # Predict
    try:
        pred = pipeline.predict(df)[0]
        result = f"Prediction: {{pred}}"
        
        if hasattr(pipeline, "predict_proba"):
            proba = pipeline.predict_proba(df)[0]
            confidence = float(max(proba))
            result += f"\\nConfidence: {{confidence:.1%}}"
            
        return result
    except Exception as e:
        return f"Prediction failed: {{e}}"

# Define inputs based on schema
inputs = []
for col in {[col.name for col in report.schema.feature_columns if col.name != report.target]}:
    inputs.append(gr.Textbox(label=col))

# Create Gradio interface
demo = gr.Interface(
    fn=predict_fn,
    inputs=inputs,
    outputs="text",
    title="Nexora Predictive Model",
    description="Auto-generated Gradio interface for predicting `{report.target}`."
)

if __name__ == "__main__":
    demo.launch(share=False)
'''
    return code


