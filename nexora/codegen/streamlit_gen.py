def generate_streamlit(report: NexoraReport, model_name: str | None = None) -> str:
    """Generate a complete Streamlit application for interactive model prediction.

    Args:
        report: Trained Nexora report.
        model_name: Optional model id, model name, or estimator class name.

    Returns:
        Complete Streamlit application code (standalone, ready to run).

    Example:
        `code = generate_streamlit(report)` → Run with `streamlit run app.py`
    """

    result, spec = _resolve_model(report, model_name)
    schema = report.schema
    dataset = Path(report.source_name).name
    metric = report.best_score_label if model_name is None else result.primary_metric
    score = result.primary_score

    # Build input fields based on feature types
    numeric_cols = schema.numeric_features
    categorical_cols = schema.categorical_features

    numeric_fields = _numeric_input_block(numeric_cols)
    categorical_fields = _categorical_input_block(categorical_cols)

